package com.dreamit.proxystat.model.entities;
import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;

/**
 * Classe representant un Duration
 * @author RAKOTOBE Sitraka Eric
 */
@Entity
public class Duration implements Serializable {

	private static final long serialVersionUID = 1L;
	
	@Id
	@GeneratedValue
	private Long id;
	
	private String name;
	
	private Integer numberOfDays;
	
	private Boolean fullSeason;

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the numberOfDays
	 */
	public Integer getNumberOfDays() {
		return numberOfDays;
	}

	/**
	 * @param numberOfDays the numberOfDays to set
	 */
	public void setNumberOfDays(Integer numberOfDays) {
		this.numberOfDays = numberOfDays;
	}

	/**
	 * @return the fullSeason
	 */
	public Boolean getFullSeason() {
		return fullSeason;
	}

	/**
	 * @param fullSeason the fullSeason to set
	 */
	public void setFullSeason(Boolean fullSeason) {
		this.fullSeason = fullSeason;
	}
	
	
}	